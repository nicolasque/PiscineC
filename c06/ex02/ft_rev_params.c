#include <unistd.h>

int	main(int argc, char const *argv[])
{
	int	j;

	while (argc > 1)
	{
		argc--;
		j = 0;
		while (argv[argc][j] != '\0')
		{
			write(1, &argv[argc][j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
